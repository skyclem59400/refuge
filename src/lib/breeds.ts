// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function getBreedsForSpecies(species: any): string[] {
  switch (species) {
    case 'cat': return CAT_BREEDS
    case 'dog': return DOG_BREEDS
    case 'horse':
    case 'pony': return HORSE_BREEDS
    case 'donkey': return DONKEY_BREEDS
    case 'goat': return GOAT_BREEDS
    case 'sheep': return SHEEP_BREEDS
    case 'pig': return PIG_BREEDS
    case 'cow': return COW_BREEDS
    case 'chicken': return CHICKEN_BREEDS
    case 'rabbit': return RABBIT_BREEDS
    case 'parrot': return PARROT_BREEDS
    case 'parakeet': return PARAKEET_BREEDS
    default: return []
  }
}
